class Fruits{
 public static void main(String[] args){
  String fruit = "Apple";
  switch(fruit){
   case "Apple":
	System.out.println("An apple a day keeps the doctor away");
	break;
   case "Banana":
	System.out.println("Bananas are good source of protein");
	break;
   case "Orange":
	System.out.println("Oranges are rich in vitamine c");
	break;
   case "Mango":
	System.out.println("Mangoes are juicy");
	break;
  }
 }
}